'use strict';

/*
 * test/benchmark/run.js — Precision/recall/F1 benchmark harness for VibeGuard.
 *
 * Scans the curated corpus in test/benchmark/corpus/ and computes per-category
 * true positives, false positives, false negatives, precision, recall, and F1.
 *
 * A finding is a TRUE POSITIVE if:
 *   - It fires on a vuln file AND its ruleId is in the expected list for that file.
 * A finding is a FALSE POSITIVE if:
 *   - It fires on a clean file (any finding), OR
 *   - It fires on a vuln file but its ruleId is NOT in the expected list.
 * A FALSE NEGATIVE is an expected ruleId that did NOT fire on its vuln file.
 *
 * Usage: node test/benchmark/run.js [--json] [--update-readme]
 */

const fs = require('fs');
const path = require('path');

const ROOT = path.resolve(__dirname, '..', '..');
const CORPUS_DIR = path.join(__dirname, 'corpus');
const MANIFEST_PATH = path.join(__dirname, 'manifest.json');

function scanFile(relPath, content) {
  const { scanFileContent } = require(path.join(ROOT, 'src', 'scanner'));
  return scanFileContent(path.join(ROOT, relPath), relPath, content, null);
}

function runBenchmark() {
  // Ensure corpus exists (gitignored — generated at runtime).
  const CORPUS_DIR = path.join(__dirname, 'corpus');
  if (!fs.existsSync(CORPUS_DIR) || fs.readdirSync(CORPUS_DIR).length === 0) {
    try { require('child_process').execSync('node test/benchmark/generate-corpus.js', { cwd: ROOT, stdio: 'pipe' }); } catch {}
  }

  const manifest = JSON.parse(fs.readFileSync(MANIFEST_PATH, 'utf8'));
  const results = {};

  for (const [category, groups] of Object.entries(manifest)) {
    let tp = 0, fp = 0, fn = 0;
    const details = [];

    // Scan vuln files — expected findings should fire.
    for (const [filename, expectedRules] of Object.entries(groups.vuln || {})) {
      const filePath = path.join(CORPUS_DIR, category, 'vuln', filename);
      const content = fs.readFileSync(filePath, 'utf8');
      const findings = scanFile(filename, content);
      const firedRuleIds = new Set(findings.map((f) => f.ruleId));
      const expectedSet = new Set(expectedRules);

      for (const ruleId of expectedRules) {
        if (firedRuleIds.has(ruleId)) {
          tp++;
          details.push({ file: filename, ruleId, verdict: 'TP' });
        } else {
          fn++;
          details.push({ file: filename, ruleId, verdict: 'FN' });
        }
      }

      // Findings on vuln files with unexpected ruleIds are false positives.
      // Low-confidence findings are hints, not alerts — exclude from FP count.
      for (const f of findings) {
        if (!expectedSet.has(f.ruleId) && f.confidence !== 'low') {
          fp++;
          details.push({ file: filename, ruleId: f.ruleId, verdict: 'FP' });
        }
      }
    }

    // Scan clean files — no medium+ confidence findings should fire.
    for (const [filename] of Object.entries(groups.clean || {})) {
      const filePath = path.join(CORPUS_DIR, category, 'clean', filename);
      const content = fs.readFileSync(filePath, 'utf8');
      const findings = scanFile(filename, content);
      for (const f of findings) {
        if (f.confidence === 'low') continue; // hints, not alerts
        fp++;
        details.push({ file: filename, ruleId: f.ruleId, verdict: 'FP' });
      }
    }

    const precision = tp + fp > 0 ? tp / (tp + fp) : 0;
    const recall = tp + fn > 0 ? tp / (tp + fn) : 0;
    const f1 = precision + recall > 0 ? (2 * precision * recall) / (precision + recall) : 0;

    results[category] = { tp, fp, fn, precision, recall, f1, details };
  }

  // Overall totals.
  let totalTp = 0, totalFp = 0, totalFn = 0;
  for (const r of Object.values(results)) {
    totalTp += r.tp;
    totalFp += r.fp;
    totalFn += r.fn;
  }
  const overallPrecision = totalTp + totalFp > 0 ? totalTp / (totalTp + totalFp) : 0;
  const overallRecall = totalTp + totalFn > 0 ? totalTp / (totalTp + totalFn) : 0;
  const overallF1 = overallPrecision + overallRecall > 0
    ? (2 * overallPrecision * overallRecall) / (overallPrecision + overallRecall)
    : 0;

  return { results, overall: { tp: totalTp, fp: totalFp, fn: totalFn, precision: overallPrecision, recall: overallRecall, f1: overallF1 } };
}

function formatPct(v) {
  return (v * 100).toFixed(1) + '%';
}

function renderMarkdown(report) {
  const lines = [];
  lines.push('# VibeGuard Benchmark Results');
  lines.push('');
  lines.push('<!-- BENCHMARK:START -->');
  lines.push('<!-- Auto-generated by `npm run benchmark` — do not edit manually -->');
  lines.push('');
  lines.push('## Summary');
  lines.push('');
  lines.push('| Category | TP | FP | FN | Precision | Recall | F1 |');
  lines.push('|----------|----|----|----|-----------|--------|----|');

  for (const [cat, r] of Object.entries(report.results)) {
    lines.push(
      `| ${cat} | ${r.tp} | ${r.fp} | ${r.fn} | ${formatPct(r.precision)} | ${formatPct(r.recall)} | ${formatPct(r.f1)} |`
    );
  }

  lines.push(
    `| **OVERALL** | **${report.overall.tp}** | **${report.overall.fp}** | **${report.overall.fn}** | **${formatPct(report.overall.precision)}** | **${formatPct(report.overall.recall)}** | **${formatPct(report.overall.f1)}** |`
  );
  lines.push('');
  lines.push('## Per-Category Details');
  lines.push('');

  for (const [cat, r] of Object.entries(report.results)) {
    lines.push(`### ${cat}`);
    lines.push('');
    lines.push('| File | Rule ID | Verdict |');
    lines.push('|------|---------|---------|');
    for (const d of r.details) {
      lines.push(`| ${d.file} | \`${d.ruleId}\` | ${d.verdict} |`);
    }
    lines.push('');
  }

  lines.push('## Methodology');
  lines.push('');
  lines.push('- **True Positive (TP)**: Expected rule fires on a vuln file.');
  lines.push('- **False Positive (FP)**: Any finding on a clean file, or an unexpected rule on a vuln file.');
  lines.push('- **False Negative (FN)**: Expected rule that did not fire on a vuln file.');
  lines.push('- **Precision** = TP / (TP + FP)');
  lines.push('- **Recall** = TP / (TP + FN)');
  lines.push('- **F1** = 2 * (Precision * Recall) / (Precision + Recall)');
  lines.push('');
  // No wall-clock timestamp: it made the report churn on every run and show as a
  // dirty file with no real change. Git history is the provenance. The report now
  // only changes when the actual metrics change.
  lines.push('_Regenerate with `npm run benchmark`._');
  lines.push('');
  lines.push('<!-- BENCHMARK:END -->');

  return lines.join('\n');
}

function printSummaryTable(report) {
  const w = (s) => String(s).padEnd(12);
  console.log('');
  console.log('  ' + w('Category') + w('TP') + w('FP') + w('FN') + w('Precision') + w('Recall') + w('F1'));
  console.log('  ' + '-'.repeat(84));
  for (const [cat, r] of Object.entries(report.results)) {
    console.log('  ' + w(cat) + w(r.tp) + w(r.fp) + w(r.fn) + w(formatPct(r.precision)) + w(formatPct(r.recall)) + w(formatPct(r.f1)));
  }
  console.log('  ' + '-'.repeat(84));
  const o = report.overall;
  console.log('  ' + w('OVERALL') + w(o.tp) + w(o.fp) + w(o.fn) + w(formatPct(o.precision)) + w(formatPct(o.recall)) + w(formatPct(o.f1)));
  console.log('');
}

// --- CLI ---

const args = process.argv.slice(2);
const report = runBenchmark();

if (args.includes('--json')) {
  console.log(JSON.stringify(report, null, 2));
} else {
  printSummaryTable(report);
}

// Write markdown report.
const mdPath = path.join(__dirname, 'benchmark-results.md');
fs.writeFileSync(mdPath, renderMarkdown(report));
console.log(`Report written to ${path.relative(ROOT, mdPath)}`);

// --gate: fail (exit 2) if overall precision/recall/F1 regress below a floor.
// Floors sit just under current measured numbers so a real regression trips CI
// but the current corpus passes. Bump these UP as the tool improves — never down
// silently. Override with VIBEGUARD_BENCH_MIN_{PRECISION,RECALL,F1}.
if (args.includes('--gate')) {
  const o = report.overall;
  const floors = {
    precision: Number(process.env.VIBEGUARD_BENCH_MIN_PRECISION || 0.80),
    recall: Number(process.env.VIBEGUARD_BENCH_MIN_RECALL || 0.78),
    f1: Number(process.env.VIBEGUARD_BENCH_MIN_F1 || 0.80),
  };
  const checks = [
    ['precision', o.precision, floors.precision],
    ['recall', o.recall, floors.recall],
    ['f1', o.f1, floors.f1],
  ];
  let failed = false;
  for (const [name, actual, floor] of checks) {
    const ok = actual >= floor;
    if (!ok) failed = true;
    console.log(`  gate ${ok ? 'PASS' : 'FAIL'}  ${name}: ${(actual * 100).toFixed(1)}% (floor ${(floor * 100).toFixed(1)}%)`);
  }
  if (failed) {
    console.error('\nBenchmark gate FAILED — detection quality regressed below floor.');
    process.exit(2);
  }
  console.log('\nBenchmark gate passed.');
}

// Update README if requested.
if (args.includes('--update-readme')) {
  const readmePath = path.join(ROOT, 'README.md');
  let readme = fs.readFileSync(readmePath, 'utf8');
  const startMarker = '<!-- BENCHMARK:START -->';
  const endMarker = '<!-- BENCHMARK:END -->';
  const md = renderMarkdown(report);

  const startIdx = readme.indexOf(startMarker);
  const endIdx = readme.indexOf(endMarker);

  if (startIdx !== -1 && endIdx !== -1) {
    // Extract just the content between markers (from the md, which has them too).
    const mdStart = md.indexOf(startMarker);
    const mdEnd = md.indexOf(endMarker) + endMarker.length;
    const block = md.slice(mdStart, mdEnd);
    readme = readme.slice(0, startIdx) + block + readme.slice(endIdx + endMarker.length);
  } else {
    // No markers yet — append before the closing.
    readme = readme.trimEnd() + '\n\n' + md + '\n';
  }

  fs.writeFileSync(readmePath, readme);
  console.log('README.md updated with benchmark results.');
}

module.exports = { runBenchmark, renderMarkdown };